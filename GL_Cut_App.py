# --- 2. ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì (‡∏¢‡∏±‡∏î‡πÉ‡∏´‡πâ‡πÅ‡∏ô‡πà‡∏ô + ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÅ‡∏ú‡πà‡∏ô‡πÉ‡∏´‡∏ç‡πà‡πÄ‡∏õ‡πá‡∏ô‡∏•‡∏≥‡∏î‡∏±‡∏ö‡πÅ‡∏£‡∏Å) ---
def run_smart_simulation(stocks, pieces, allowance, trials=50):
    best_overall_results = None
    max_score = -1  # ‡πÉ‡∏ä‡πâ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏Ñ‡∏ß‡∏≤‡∏°‡∏Ñ‡∏∏‡πâ‡∏°‡∏Ñ‡πà‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏±‡∏î‡∏™‡∏¥‡∏ô

    progress_text = "‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏´‡∏≤‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡∏ó‡∏µ‡πà‡∏Ñ‡∏∏‡πâ‡∏°‡∏Ñ‡πà‡∏≤‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î (‡πÄ‡∏ô‡πâ‡∏ô‡πÅ‡∏ú‡πà‡∏ô‡πÉ‡∏´‡∏ç‡πà)..."
    my_bar = st.progress(0, text=progress_text)

    # ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏Ñ‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å‡πÉ‡∏´‡∏ç‡πà‡πÑ‡∏õ‡πÄ‡∏•‡πá‡∏Å ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏£‡∏∞‡∏ö‡∏ö‡πÄ‡∏´‡πá‡∏ô‡πÅ‡∏ú‡πà‡∏ô‡πÉ‡∏´‡∏ç‡πà‡πÄ‡∏õ‡πá‡∏ô‡∏•‡∏≥‡∏î‡∏±‡∏ö‡πÅ‡∏£‡∏Å‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏û‡∏¥‡∏à‡∏≤‡∏£‡∏ì‡∏≤
    sorted_stocks = sorted(stocks, key=lambda x: x['w'] * x['h'], reverse=True)

    for trial in range(trials):
        current_pieces = pieces.copy()
        random.shuffle(current_pieces)  # ‡∏™‡∏•‡∏±‡∏ö‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏ä‡∏¥‡πâ‡∏ô‡∏á‡∏≤‡∏ô‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏´‡∏≤‡∏à‡∏∏‡∏î‡∏ó‡∏µ‡πà "‡∏•‡∏á‡∏ï‡∏±‡∏ß" ‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î

        # ‡πÉ‡∏ä‡πâ‡πÇ‡∏´‡∏°‡∏î Offline + Global Bin Fit ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏†‡∏≤‡∏û‡∏£‡∏ß‡∏°‡∏ó‡∏±‡πâ‡∏á‡πÇ‡∏õ‡∏£‡πÄ‡∏à‡∏Å‡∏ï‡πå
        packer_engine = newPacker(
            mode=packer.PackingMode.Offline,
            bin_algo=packer.PackingBin.BBF,  # Best-Bin-Fit (‡∏â‡∏•‡∏≤‡∏î‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÅ‡∏ú‡πà‡∏ô)
            pack_algo=packer.MaxRectsBssf,
            rotation=True
        )

        for s in sorted_stocks:
            packer_engine.add_bin(s['w'], s['h'], count=100)

        for i, p in enumerate(current_pieces):
            packer_engine.add_rect(p['w'] + allowance, p['h'] + allowance, rid=i)

        packer_engine.pack()

        current_results = []
        total_used_area = 0
        total_bin_area = 0

        for b in packer_engine:
            if len(b) > 0:
                bin_area = b.width * b.height
                used_area_in_bin = sum((r.width - allowance) * (r.height - allowance) for r in b)

                total_used_area += used_area_in_bin
                total_bin_area += bin_area

                current_results.append({
                    'width': b.width,
                    'height': b.height,
                    'area': bin_area,
                    'used_area': used_area_in_bin,
                    'rects': [{'w': r.width - allowance, 'h': r.height - allowance} for r in b]
                })

        if current_results:
            # ‡∏™‡∏π‡∏ï‡∏£‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô: ‡πÄ‡∏ô‡πâ‡∏ô‡∏õ‡∏£‡∏∞‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡∏†‡∏≤‡∏û‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà (Efficiency) ‡πÄ‡∏õ‡πá‡∏ô‡∏´‡∏•‡∏±‡∏Å
            efficiency = total_used_area / total_bin_area

            # ‡∏ñ‡πâ‡∏≤‡∏Ñ‡∏ß‡∏≤‡∏°‡∏Ñ‡∏∏‡πâ‡∏°‡∏Ñ‡πà‡∏≤‡∏î‡∏µ‡∏Å‡∏ß‡πà‡∏≤‡πÄ‡∏î‡∏¥‡∏° ‡πÉ‡∏´‡πâ‡πÄ‡∏Å‡πá‡∏ö‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏ô‡∏µ‡πâ‡πÑ‡∏ß‡πâ
            if efficiency > max_score:
                max_score = efficiency
                best_overall_results = current_results

        my_bar.progress((trial + 1) / trials, text=progress_text)

    my_bar.empty()
    return best_overall_results


